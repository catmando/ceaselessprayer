  def record_prayer
    return unless @still_praying

    IpAddress.then { |address| Prayer.create(ip: address) }
    @still_praying = nil
  end

  after_mount do
    @still_praying = true
    record_prayer
    every(15.seconds) { record_prayer }
    @handle_scroll = -> { @still_praying = true }
    `#{dom_node}.addEventListener('scroll', #{@handle_scroll})`
  end

  before_unmount do
    `#{dom_node}.removeEventListener('scroll', #{@handle_scroll})`
  end

